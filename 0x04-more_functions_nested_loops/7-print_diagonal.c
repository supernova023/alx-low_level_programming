#include <stdio.h>
#include "main.h"
/**
 * print_diagonal - Extra point
 * Description - print diagonal line n characters long
 * @n: variable determines line length
 *
 *
 * Return
 */

void print_diagonal(int n)
{
	int x, y;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (y = 0; y < n; y++)
		{
			for (x = 0; x <= y; x++)
			{
				if (x == y)
				{
					_putchar('\\');
				}
				else
				{
					_putchar(' ');
				}
			}
			_putchar('\n');
		}
	}

}
