#include "main.h"
/**
 * reverse_array - Entry point
 *
 * @a: variable storing array to be reversed
 * @n: variable storing size of array
 * Return: none
 */
void reverse_array(int *a, int n)
{
	int i, j, temp;

	j = n - 1;

	for (i = 0; i < j; i++)
	{
		temp = a[i];

		a[i] = a[j];
		a[j] = temp;
		j--;
	}
}
